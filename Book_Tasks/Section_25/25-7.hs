-- Да се дефинира функция, която по даден списък от цели числа l връща
-- списък от всички двойки (a, b) от l, за които a и b са съседни елементи
-- в l и a < b.

func :: [Int] -> [(Int, Int)]
func [] = []  
func [_] = [] 
func (x:y:xs)
  | x < y     = (x, y) : func (y:xs)
  | otherwise = func (y:xs)


-- [1,2,4,3] -> [(1,2), (2,4)] 